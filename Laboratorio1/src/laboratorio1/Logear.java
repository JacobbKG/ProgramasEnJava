    private void btnLogearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogearActionPerformed
        String Usuario = txtUser.getText().trim();
        String Contra = txtContra.getText().trim();
        
        if(!validarUsuario(Usuario)) {
                JOptionPane.showMessageDialog(null, 
                                    "Usuario inválido. Debe seguir el formato #NNN-L (ej: #658-SF).", 
                                    "Error de Registro", 
                                    JOptionPane.ERROR_MESSAGE);
            return;
        }
       
        if(!validarContrasena(Contra)) {
                JOptionPane.showMessageDialog(null, 
                                    "Contraseña inválida. Debe seguir el formato $AAA@BBBB.CC (ej: $4df@4584.22).", 
                                    "Error de Registro", 
                                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!usuarioNoRegistrado(Usuario)) {
                JOptionPane.showMessageDialog(null, 
                                    "El usuario no está registrado. Intente con otro usuario.", 
                                    "Error de Registro", 
                                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!contrasenaNoRegistrada(Contra)) {
        JOptionPane.showMessageDialog(null, 
                            "La contraseña no está registrada. Intente con otra contraseña.", 
                            "Error de Registro", 
                            JOptionPane.ERROR_MESSAGE);
        return;
        }
        
      
        
       
        
      
        Menu menuform = new Menu();
        menuform.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnLogearActionPerformed

