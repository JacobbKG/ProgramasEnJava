    public static boolean validarUsuario(String usuario) {
        String regex = "#\\d{3}-[A-Z]{2}";
        return usuario.matches(regex);
    }
     
    public static boolean validarContrasena(String contrasena) {
        String regex = "\\$\\w{3}@\\d{4}\\.\\d{2}";
        return contrasena.matches(regex);
    }
    
    private boolean usuarioNoRegistrado(String usuario) {
        try (BufferedReader reader = new BufferedReader(new FileReader(FILE_NAME))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(",");
                if (datos.length > 0 && datos[0].equals(usuario)) {
                    return true; 
                }
            }
        } catch (IOException e) {
            System.out.println("Error al leer el archivo: " + e.getMessage());
        }   
        return false; 
    }
    
    private boolean contrasenaNoRegistrada(String contrasena) {
        try (BufferedReader reader = new BufferedReader(new FileReader(FILE_NAME))) {
            String linea;
            while ((linea = reader.readLine()) != null) {
                String[] datos = linea.split(",");
                if (datos.length > 1 && datos[1].equals(contrasena)) {
                    return true; 
                }
            }
        }catch (IOException e) {
            System.out.println("Error al leer el archivo: " + e.getMessage());
        }
        return false; 
    }

